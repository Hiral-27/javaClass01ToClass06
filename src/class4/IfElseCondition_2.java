public class IfElseCondition_2 {
    public static void main(String[] args) {
        //Boolean
        boolean rain = true;
        if (rain) {
            System.out.println("Lets take an Umbrella");
        } else {
            System.out.println("Lets take a hat");
        }

        // Create a Boolean variable break if it is true, then print lets take the break
        boolean breaktime = true;
        if (breaktime) {
            System.out.println("Lets take the break");
        } else {
            System.out.println("Lets continue the class");
        }


    }
}
