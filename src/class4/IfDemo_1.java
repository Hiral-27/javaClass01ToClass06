package class4;

public class IfDemo_1 {
    public static void main(String[] args) {
        // int
        int money = 15000;
        if (money > 10000) {
            System.out.println("Lets buy an Iphone");
        }
        if (money > 2000) {
            System.out.println("Lets buy a Macbook");
        }


        //Boolean
        boolean mothersDay = true;
        if (mothersDay) {
            System.out.println("Happy Mothers' Day");
        }


        //String
        String name = "Hiral";
        if (name.equals("Hiral")) {    // Case Sensitive
            System.out.println("I love to Dance");
        }

    }
}
