package class9;

public class J_01_RecapNestedLoop {
    public static void main(String[] args) {
        /*
        Question 1:
        # # # # #
        # # # # #
        # # # # #
        # # # # #
        # # # # #
         */
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 5; j++) {
                System.out.print("# ");
            }
            System.out.println();
        }

    }
}
