package class9;

public class classTask01 {
    public static void main(String[] args) {
        /*
       Task 1:
        add numbers from 0 to 10
         */

        int add=0;
        for (int i = 0; i < 10 ; i++) {
            add+= i;
        }
        System.out.println("Sum of 0 to 10 = "+add);
    }
}
