package class3;

public class ShortHandOperator_4 {
    public static void main(String[] args) {
        //*** Shortcuts also known as Short Hand Operators
        int number= 10;
        //number=number+10;
        //number+=20; // It's a shortcut the Output will be similar to the line 7, Whatever value is stored add 20 to it.
        number*=20;
        System.out.println(number);

        int number2= 20;
        number2-=10;
        System.out.println(number2);

        int number3= 100;
        number3/=10;
        System.out.println(number3);
    }
}
